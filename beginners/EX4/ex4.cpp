#include <bits/stdc++.h>

using namespace std;

int main() {
    // 一年の秒数
    int seconds = 365 * 24 * 60 * 60;
    int year_for2 = seconds * 2;
    int year_for5 = seconds * 5;
    int year_for10 = seconds * 10;

    // 以下のコメント/* */を消して追記する
    cout << seconds << endl;
    cout << year_for2 << endl;
    cout << year_for5 << endl;
    cout << year_for10 << endl;
}